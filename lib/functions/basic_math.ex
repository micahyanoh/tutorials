defmodule Tutorials.Functions.BasicMath do
    alias Tutorials.Functions.BasicMath
    def add(number_1,number_2) do
        number_1 + number_2
    end

    def subtract(number_1,number_2) do
        number_1 - number_2
    end

    def multiply(x,y), do: x*y

    def divide(a,b) do
        a/b
    end
        
end